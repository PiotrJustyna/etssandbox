-module(etssandbox_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
  io:format("Starting.~n"),
  etssandbox_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
  io:format("Stopping.~n"),
  ok.

